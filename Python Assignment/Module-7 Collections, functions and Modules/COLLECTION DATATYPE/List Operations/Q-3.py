# Write a Python program to add elements to a list using insert() and append().

# append()
my_list = [] # taking an empty list

my_list.append(1)
my_list.append(2)
my_list.append(3)
print(my_list) # Output : [1, 2, 3]

# insert
fruits = ['apple', 'banana']
fruits.insert(1, 'cherry') # it adds element after the 0 index element

print(fruits)  # Output: ['apple', 'cherry', 'banana']